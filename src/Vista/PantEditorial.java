    public PantEditorial() {
        initComponents();
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jtbl_editorial.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;
            ConectaDb conn = new ConectaDb();
            Connection con = (Connection) conn.getConnection();
            //SELECT idautores,nombre,paterno,materno FROM autores

            String sql = "SELECT ideditorial,nombre FROM editorial  ";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("Codigo");
            modelo.addColumn("Editorial");            
            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];

                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
    }

