    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        PantEditorialEdit menu = new PantEditorialEdit();
        menu.setVisible(true);
        this.setVisible(false);
        
        PreparedStatement ps = null;
        ResultSet rs = null;
        try {

            menu.setVisible(true);
            this.setVisible(false);
            ConectaDb objCon = new ConectaDb();
            Connection conn = (Connection) objCon.getConnection();

            int Fila = jtbl_editorial.getSelectedRow();
            ideditorial = jtbl_editorial.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("SELECT nombre FROM editorial WHERE ideditorial=?");
            ps.setString(1, ideditorial);
            rs = ps.executeQuery();
            while (rs.next()) {
                PantEditorialEdit.jtxActuaEditorial.setText(rs.getString("nombre"));
                
            }

        } catch (SQLException ex) {
            System.out.println(ex.getMessage());

        }
    }//GEN-LAST:event_btn_editarActionPerformed

