    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        // TODO add your handling code here:
        
        com.mysql.jdbc.PreparedStatement ps=null;
        try {
            ConectaDb objCon=new ConectaDb();
            java.sql.Connection conn= objCon.getConnection();
            ps=(com.mysql.jdbc.PreparedStatement) conn.prepareStatement("UPDATE alumno SET  nombre= ?, paterno= ?, materno= ?,dni=?,direccion=?,telefono=? WHERE idAlumno= ?");
            
            ps.setString(1, jtxNomActAlum.getText());
            ps.setString(2, jtxPateActAlum.getText());
            ps.setString(3, jtxMateActuAlum.getText());
            ps.setString(4, jtxDniActAlum.getText());
            ps.setString(5, jtxDirecActAlum.getText());
            ps.setString(6, jtxTelefActAlum.getText());
            ps.setString(7,PantAlumno.idalumno );
            
            ps.execute();
            
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        PantAlumno menu = new PantAlumno();
        menu.setVisible(true);
        this.setVisible(false);
        
        
    }//GEN-LAST:event_btn_actualizarActionPerformed

